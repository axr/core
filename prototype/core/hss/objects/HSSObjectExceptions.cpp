#include "HSSObjectExceptions.h"

using namespace AXR;

/**
 *  @brief This class is deprecated. Use AXRError instead.
 *
 *  @todo remove this, and fix any dependency
 */
HSSUnknownObjectTypeException::HSSUnknownObjectTypeException(std::string type)
{
    this->type = type;
}

std::string HSSUnknownObjectTypeException::toString()
{
    return "HSSObject Error: unknown object type "+this->type+"while trying to create a new object";
}

std::string HSSUnknownValueTypeException::toString()
{
    return "HSSValue Error: unkown value type";
}

HSSObjectNotFoundException::HSSObjectNotFoundException(std::string name)
{
    this->name = name;
}

std::string HSSObjectNotFoundException::toString()
{
    return "Object with name "+this->name+" not found";
}


